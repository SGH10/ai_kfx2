    public WorkflowModels.DraftResponse generateDraft(WorkflowModels.DraftRequest request) {
        SettingsModels.AppSettings settings = settingsService.getSettings();
        SettingsModels.AiSettings ai = settings.ai();
        SettingsModels.MailSettings mail = settings.mail();

        String companyName = fallback(request.companyName(), ai.defaultCompanyName());
        String productName = fallback(request.productName(), ai.defaultProductName());
        String valueProposition = fallback(request.valueProposition(), ai.defaultValueProposition());
        String language = fallback(request.language(), ai.defaultLanguage());
        String tone = fallback(request.tone(), ai.defaultTone());
        String callToAction = fallback(request.callToAction(), ai.defaultCallToAction());
        String senderName = fallback(mail.senderName(), "John");
        String senderEmail = fallback(mail.senderEmail(), "");

        List<WorkflowModels.CustomerLead> recipients = safeRecipients(request.recipients());
        WorkflowModels.CustomerLead firstRecipient = recipients.isEmpty()
                ? new WorkflowModels.CustomerLead(
                "placeholder",
                "Selected Prospect",
                "",
                "Target Market",
                "Business Contact",
                "",
                "Website",
                "Generated from current settings"
        )
                : recipients.get(0);

        String systemPrompt = buildDraftSystemPrompt(language, tone);
        String userPrompt = buildDraftUserPrompt(
                companyName,
                productName,
                valueProposition,
                callToAction,
                language,
                recipients,
                firstRecipient,
                senderName,
                senderEmail,
                request.template()
        );

        log.info("Generating draft with AI. recipients={}, provider={}, model={}, targetLanguage={}",
                recipients.size(), ai.provider(), ai.model(), language);
        String raw = aiCompletionService.complete(ai, systemPrompt, userPrompt);
        ParsedDraft parsedDraft = parseDraft(raw);

        String analysis = "Generated by real AI using the configured Base URL / API Key / Model.";
        List<String> followUpTips = buildFollowUpTips(firstRecipient, recipients.size());
        String subject = cleanGeneratedText(
                fallback(parsedDraft.subject(), buildFallbackSubject(companyName, productName, firstRecipient.companyName())),
                companyName,
                productName,
                firstRecipient,
                senderName
        );
        String body = cleanGeneratedBody(cleanGeneratedText(
                fallback(parsedDraft.body(), raw),
                companyName,
                productName,
                firstRecipient,
                senderName
        ));

        return new WorkflowModels.DraftResponse(
                subject,
                body,
                analysis,
                followUpTips
        );
    }

    public WorkflowModels.TranslateEmailResponse translateEmail(WorkflowModels.TranslateEmailRequest request) {
        SettingsModels.AiSettings ai = settingsService.getSettings().ai();

        String targetLanguage = fallback(request.targetLanguage(), ai.defaultLanguage());
        String targetLanguageName = resolveLanguageName(targetLanguage);

        String systemPrompt = buildTranslationSystemPrompt(targetLanguageName);
        String userPrompt = buildTranslationUserPrompt(
                fallback(request.subject(), ""),
                fallback(request.body(), ""),
                targetLanguageName
        );

        log.info("Translating email with AI. provider={}, model={}, targetLanguage={}",
                ai.provider(), ai.model(), targetLanguage);
        String raw = aiCompletionService.complete(ai, systemPrompt, userPrompt);
        ParsedDraft parsedDraft = parseDraft(raw);

        return new WorkflowModels.TranslateEmailResponse(
                fallback(parsedDraft.subject(), request.subject()),
                fallback(parsedDraft.body(), request.body())
        );
    }


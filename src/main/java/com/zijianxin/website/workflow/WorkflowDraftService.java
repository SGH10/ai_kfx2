    private String buildTemplatePromptSection(
            WorkflowModels.EmailTemplateContext template,
            String languageName,
            boolean nonChinese
    ) {
        if (template == null) {
            return "";
        }

        String name = fallback(template.name(), "Selected template");
        String scenario = fallback(template.scenario(), "");
        String subject = fallback(template.subject(), "");
        String body = fallback(template.body(), "");
        String instruction = fallback(template.instruction(), "");
        if (subject.isBlank() && body.isBlank() && instruction.isBlank()) {
            return "";
        }

        String translateDirective = nonChinese && containsChinese(subject + body + instruction)
                ? "\nTemplate translation rule: The selected template contains Chinese text. Use its structure and intent, but translate all Chinese text into " + languageName + "."
                : "";

        return """

                Selected email template:
                Template name: %s
                Scenario: %s
                Template subject:
                %s

                Template body:
                %s

                Template usage notes:
                %s%s
                Template rule: Use this template as loose structure and messaging guidance, not as a fill-in-the-blank form. Replace placeholders naturally with the sender and recipient context. Do not output unresolved placeholders. Improve the wording when the context is thin, and avoid generic empty claims.
                """.formatted(name, scenario, subject, body, instruction, translateDirective);
    }

    private String cleanRecipientContactName(String contactName) {
        String normalized = fallback(contactName, "")
                .replaceAll("[\\p{Cntrl}\\uFFFD]+", " ")
                .replaceAll("[\\p{Punct}&&[^.]]+", " ")
                .replaceAll("\\s+", " ")
                .trim();
        if (normalized.isBlank()) {
            return "";
        }

        String lower = normalized.toLowerCase();
        if (JUNK_CONTACT_TOKENS.stream().anyMatch(token -> lower.equals(token) || lower.contains(token))) {
            return "";
        }
        if (normalized.length() > 32 || normalized.matches(".*\\d{3,}.*") || normalized.contains("@")) {
            return "";
        }

        boolean englishPersonName = normalized.matches("(?i)(mr\\.?|ms\\.?|mrs\\.?|dr\\.?)?\\s*[A-Z][a-z]+(?:\\s+[A-Z][a-z]+){0,2}");
        boolean likelyChinesePersonName = normalized.matches("[\\u4e00-\\u9fff]{2,4}") && !normalized.endsWith("公司");
        return englishPersonName || likelyChinesePersonName ? normalized : "";
    }

    private String cleanGeneratedBody(String body) {
        String cleaned = fallback(body, "");
        if (cleaned.isBlank()) {
            return cleaned;
        }

        cleaned = cleaned.replaceFirst(
                "^[\\s\\p{Punct}a-zA-Z]{0,8}(?:联系我们|联系方式|关于我们|首页|Contact\\s*Us|Contacts|About\\s*Us|Home|Business\\s*Contact)\\s*(您好|你好|您好，|你好，|Hi|Hello|Dear)",
                "$1"
        );
        cleaned = cleaned.replaceFirst("^(您好|你好)\\s+", "$1，");
        return cleaned.trim();
    }

    private String cleanGeneratedText(
            String text,
            String companyName,
            String productName,
            WorkflowModels.CustomerLead recipient,
            String senderName
    ) {
        String cleaned = fallback(text, "");
        if (cleaned.isBlank()) {
            return cleaned;
        }

        String contactName = cleanRecipientContactName(recipient.contactName());
        cleaned = cleaned
                .replace("{{senderName}}", fallback(senderName, ""))
                .replace("{{senderCompany}}", fallback(companyName, ""))
                .replace("{{recipientCompany}}", fallback(recipient.companyName(), ""))
                .replace("{{productName}}", fallback(productName, ""))
                .replace("{{contactName}}", contactName)
                .replace("{{valueProposition}}", "");
        return cleaned.replaceAll("\\{\\{[^}]+}}", "").trim();
    }

    private ParsedDraft parseDraft(String raw) {
        String subject = "";
        String body = raw == null ? "" : raw.trim();

        if (raw == null || raw.isBlank()) {
            return new ParsedDraft(subject, body);
        }

        String[] lines = raw.split("\\R");
        StringBuilder bodyBuilder = new StringBuilder();
        boolean bodyStarted = false;

        for (String line : lines) {
            String trimmed = line.trim();
            if (!bodyStarted && trimmed.regionMatches(true, 0, "SUBJECT:", 0, 8)) {
                subject = trimmed.substring(8).trim();
                continue;
            }
            if (trimmed.equalsIgnoreCase("BODY:")) {
                bodyStarted = true;
                continue;
            }

            if (bodyBuilder.length() > 0) {
                bodyBuilder.append(System.lineSeparator());
            }
            bodyBuilder.append(line);
        }

        if (bodyBuilder.length() > 0) {
            body = bodyBuilder.toString().trim();
        }

        return new ParsedDraft(subject, body);
    }

    private List<String> buildFollowUpTips(WorkflowModels.CustomerLead recipient, int recipientCount) {
        List<String> tips = new ArrayList<>();
        tips.add("24 小时内如果没有回复，可发送一次简短跟进。");
        tips.add("对 " + fallback(recipient.companyName(), "目标客户") + " 这类高匹配客户，建议补充案例或报价信息。");
        if (recipientCount > 1) {
            tips.add("当前草稿适用于 " + recipientCount + " 个已选客户，发送前建议再检查变量是否自然。");
        } else {
            tips.add("当前草稿更适合单客户精细化发送，建议再补一条个性化开场。");
        }
        return tips;
    }

    private String buildFallbackSubject(String companyName, String productName, String recipientCompany) {
        return recipientCompany + " x " + companyName + " | " + productName;
    }

    private String resolveLanguageName(String language) {
        if (language == null || language.isBlank()) {
            return "Simplified Chinese";
        }
        return switch (language.trim()) {
            case "zh", "zh-CN", "zh-Hans" -> "Simplified Chinese";
            case "en", "en-US", "en-GB" -> "English";
            case "de", "de-DE" -> "German";
            case "fr", "fr-FR" -> "French";
            case "es", "es-ES" -> "Spanish";
            case "ru", "ru-RU" -> "Russian";
            case "ar", "ar-SA", "ar-AE" -> "Arabic";
            case "pt", "pt-BR", "pt-PT" -> "Portuguese";
            default -> language.trim();
        };
    }

    private boolean isNonChineseTarget(String language) {
        if (language == null || language.isBlank()) {
            return false;
        }
        String trimmed = language.trim();
        return !(trimmed.equals("zh") || trimmed.equals("zh-CN") || trimmed.equals("zh-Hans"));
    }

    private boolean containsChinese(String text) {
        if (text == null || text.isBlank()) {
            return false;
        }
        return text.codePoints().anyMatch(cp -> cp >= '\u4e00' && cp <= '\u9fff');
    }

    private List<WorkflowModels.CustomerLead> safeRecipients(List<WorkflowModels.CustomerLead> recipients) {
        return recipients == null ? List.of() : recipients;
    }

    private String fallback(String value, String fallbackValue) {
        return value == null || value.isBlank() ? fallbackValue : value.trim();
    }

